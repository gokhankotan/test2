/**
 * i18n çeviri yardımcısı.
 * @param {string} key - Çeviri anahtarı
 * @param {string} lang - Aktif dil ('tr' | 'en')
 * @param {Object} replacements - Dinamik değişkenler (örn: {count: 5})
 */
export function t(key, lang = 'tr', replacements = {}) {
  const dict = translations[lang] || translations['tr'];
  let text = dict[key] || translations['tr'][key] || key;
  
  // Değişkenleri eşle (Örn: {count} -> 5)
  Object.keys(replacements).forEach(k => {
    text = text.replace(new RegExp(`{${k}}`, 'g'), replacements[k]);
  });
  
  return text;
}
